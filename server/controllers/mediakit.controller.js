import MediaKit from '../models/MediaKit.js';
import puppeteer from 'puppeteer';

export const getMediaKit = async (req, res, next) => {
  try {
    let mediaKit = await MediaKit.findOne({ userId: req.user._id });
    if (!mediaKit) {
      mediaKit = await MediaKit.create({ userId: req.user._id });
    }
    res.json(mediaKit);
  } catch (error) {
    next(error);
  }
};

export const updateMediaKit = async (req, res, next) => {
  try {
    const { niche, platforms, audienceAge, audienceGender, topContent, pastCollabs, rateCard } = req.body;

    const mediaKit = await MediaKit.findOneAndUpdate(
      { userId: req.user._id },
      { niche, platforms, audienceAge, audienceGender, topContent, pastCollabs, rateCard },
      { new: true, upsert: true, runValidators: true }
    );

    res.json(mediaKit);
  } catch (error) {
    next(error);
  }
};

export const generatePDF = async (req, res, next) => {
  try {
    const mediaKit = await MediaKit.findOne({ userId: req.user._id }).populate('userId', 'name avatar username bio');
    if (!mediaKit) {
      return res.status(404).json({ message: 'Media kit not found' });
    }

    const html = `
      <!DOCTYPE html>
      <html>
      <head>
        <style>
          * { margin: 0; padding: 0; box-sizing: border-box; }
          body { font-family: 'Segoe UI', sans-serif; padding: 40px; color: #1a1a1a; }
          .header { text-align: center; margin-bottom: 40px; }
          .avatar { width: 100px; height: 100px; border-radius: 50%; object-fit: cover; }
          h1 { font-size: 28px; margin-top: 15px; }
          .username { color: #666; font-size: 14px; }
          .bio { color: #444; margin-top: 10px; font-size: 14px; }
          .section { margin-top: 30px; }
          .section h2 { font-size: 18px; color: #7c3aed; border-bottom: 2px solid #7c3aed; padding-bottom: 5px; }
          .platform { display: flex; justify-content: space-between; padding: 10px 0; border-bottom: 1px solid #eee; }
          .collab { padding: 10px 0; border-bottom: 1px solid #eee; }
          .rate-card { background: #f5f5f5; padding: 20px; border-radius: 8px; margin-top: 10px; white-space: pre-line; }
        </style>
      </head>
      <body>
        <div class="header">
          ${mediaKit.userId.avatar ? `<img src="${mediaKit.userId.avatar}" class="avatar" />` : ''}
          <h1>${mediaKit.userId.name}</h1>
          <div class="username">@${mediaKit.userId.username}</div>
          ${mediaKit.userId.bio ? `<div class="bio">${mediaKit.userId.bio}</div>` : ''}
        </div>

        ${mediaKit.niche ? `
        <div class="section">
          <h2>Niche</h2>
          <p>${mediaKit.niche}</p>
        </div>` : ''}

        ${mediaKit.platforms && mediaKit.platforms.length > 0 ? `
        <div class="section">
          <h2>Platform Performance</h2>
          ${mediaKit.platforms.map(p => `
            <div class="platform">
              <span>${p.name}</span>
              <span>${p.followers.toLocaleString()} followers • ${p.engagementRate}% engagement</span>
            </div>
          `).join('')}
        </div>` : ''}

        ${mediaKit.audienceAge || mediaKit.audienceGender ? `
        <div class="section">
          <h2>Audience</h2>
          ${mediaKit.audienceAge ? `<p>Age: ${mediaKit.audienceAge}</p>` : ''}
          ${mediaKit.audienceGender ? `<p>Gender: ${mediaKit.audienceGender}</p>` : ''}
        </div>` : ''}

        ${mediaKit.pastCollabs && mediaKit.pastCollabs.length > 0 ? `
        <div class="section">
          <h2>Past Collaborations</h2>
          ${mediaKit.pastCollabs.map(c => `
            <div class="collab">
              <strong>${c.brand}</strong>
              <p>${c.description}</p>
            </div>
          `).join('')}
        </div>` : ''}

        ${mediaKit.rateCard ? `
        <div class="section">
          <h2>Rate Card</h2>
          <div class="rate-card">${mediaKit.rateCard}</div>
        </div>` : ''}

        <div style="margin-top: 40px; text-align: center; color: #999; font-size: 12px;">
          Generated by CreatorPage
        </div>
      </body>
      </html>
    `;

    const browser = await puppeteer.launch({ headless: true });
    const page = await browser.newPage();
    await page.setContent(html);
    const pdfBuffer = await page.pdf({ format: 'A4', printBackground: true });
    await browser.close();

    mediaKit.lastGeneratedAt = new Date();
    await mediaKit.save();

    res.setHeader('Content-Type', 'application/pdf');
    res.setHeader('Content-Disposition', `attachment; filename="${mediaKit.userId.username}_media_kit.pdf"`);
    res.send(pdfBuffer);
  } catch (error) {
    next(error);
  }
};